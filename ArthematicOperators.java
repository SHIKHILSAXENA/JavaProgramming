public class ArthematicOperators {

    public static void main(String[] args) {
        //Arthematic Operators

        int a = 9;
        int b = 10 % a;
        System.out.println(b);

        //Assignment operators
        int c = 50;
        c *= 10;
        System.out.println(c);

        //Comparison

        System.out.println(6<=64);

        //Logical Operator
        System.out.println(6<=64 && 6<5);
        System.out.println(6<=64 || 64<55);
        System.out.println(6!=60);


    }
}
