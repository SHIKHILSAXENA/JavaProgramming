public class Loops {
    public static void main(String[] args) {
        int i = 1;
        System.out.println("Enter loops");

        while (i<=3){
            System.out.println(i);
            i++;
        }
        System.out.println("exit loops!!!");

        int a = 100;
        System.out.println("Enter natural number");

        while (a<=200){
            System.out.println(a);
            a++;
        }
        System.out.println("exit loops!!!");
    }
}
