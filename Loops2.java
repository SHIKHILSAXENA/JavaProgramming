public class Loops2 {
    public static void main(String[] args) {
        int i = 0;
        do {
            System.out.println(i);
            i++;
        }
        while (i<5);

        int j = 1;
        do {
            System.out.println(j);
            j++;
        }
        while (j<=10);
    }
}
