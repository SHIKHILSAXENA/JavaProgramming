public class Loops3 {
    public static void main(String[] args) {
        for (int i = 1;i<=10;i++)
            System.out.println(i);

        int n = 5;
        for (int i = 0; i<n;i++)
            System.out.println(2*i+1);

        for (int a = 5;a!=0;a--)
            System.out.println(a);

        for (int j=10;j!=0;j--)
            System.out.println(j);
    }
}
